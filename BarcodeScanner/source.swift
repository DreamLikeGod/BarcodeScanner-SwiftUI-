# Code Scanner App

Минимальная iOS 16+, SwiftUI, MVVM реализция тестового задания.

## Что внутри
- AVFoundation scanner (UIViewRepresentable)
- OpenFoodFacts интеграция
- Core Data (программная модель)
- MVVM (UseCase + Repository)
- Swift Concurrency (async/await)

## Как запустить
1. Открой проект в Xcode 14+ (iOS 16 SDK).
2. В Target -> Signing установи свою команду разработчика.
3. Разреши Camera Usage Description в Info.plist:
   - `NSCameraUsageDescription` = "Необходимо для сканирования штрих- и QR-кодов"
4. Собери и запусти на реальном устройстве (симулятор не поддерживает камеру).

